            <p>De acuerdo con las regulaciones de la plataforma de desarrolladores de Facebook (Meta), proporcionamos a todos nuestros usuarios un método claro y directo para solicitar la eliminación completa de toda la información personal obtenida a través de la autenticación social en nuestra aplicación.</p>

            <h2>1. Cómo desvincular el acceso a través de Facebook</h2>
            <p>Si has iniciado sesión utilizando tu cuenta de Facebook y deseas revocar el acceso a tus datos de perfil, sigue estos pasos:</p>
            <ol>
                <li>Ingresa a tu cuenta de Facebook en la aplicación o página web.</li>
                <li>Ve a <strong>Configuración y privacidad</strong> y luego haz clic en <strong>Configuración</strong>.</li>
                <li>En el menú de la izquierda, selecciona <strong>Aplicaciones y sitios web</strong>.</li>
                <li>Busca la aplicación <strong>Turismo Norte de Santander</strong> en el listado.</li>
                <li>Haz clic en el botón <strong>Eliminar</strong> al lado de la aplicación para desvincularla por completo.</li>
            </ol>

            <div class="highlight">
                <p><strong>¿Qué sucede al desvincularla?</strong> Facebook dejará de proporcionar acceso a tu dirección de correo electrónico y datos básicos del perfil de red social en los inicios de sesión futuros.</p>
            </div>

            <h2>2. Cómo solicitar el borrado permanente de tu cuenta y datos</h2>
            <p>Si deseas que eliminemos de forma definitiva tu registro de usuario, historial de favoritos, y toda la información asociada a tu perfil de nuestra base de datos, puedes realizarlo a través de los siguientes métodos:</p>
            
            <h3>Opción A: Desde la Aplicación Móvil</h3>
            <p>Puedes ir a tu <strong>Perfil</strong> -> <strong>Ajustes de Cuenta</strong> y pulsar sobre la opción <strong>Eliminar cuenta definitivamente</strong>. Tu cuenta y datos asociados se purgarán de inmediato de nuestros servidores de producción.</p>

            <h3>Opción B: Solicitud por Correo Electrónico</h3>
            <p>Puedes enviar una solicitud explícita de eliminación de datos a nuestro equipo técnico:</p>
            <ul>
                <li>Envía un correo electrónico a: <strong>soporte@nortedesantander.com</strong></li>
                <li>Asunto del correo: <strong>Solicitud de Eliminación de Datos de Usuario</strong></li>
                <li>En el mensaje, indica el correo electrónico con el que te registraste en la aplicación para proceder a la verificación de propiedad e inicio de borrado.</li>
            </ul>

            <p>Una vez recibida y confirmada la solicitud del correo, nuestro equipo de soporte técnico procederá a eliminar permanentemente tu registro en un plazo máximo de <strong>48 horas hábiles</strong>.</p>

